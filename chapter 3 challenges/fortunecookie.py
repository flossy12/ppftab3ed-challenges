# Fortune Cookie Program

import random

cookie = random.randint(1,5)

if cookie == 1:
	print("You are going to have an awesome day")
	input()

elif cookie == 2:
	print("Great misfortune will find you today")
	input()

elif cookie == 3:
	print("You will meet a man with green eyes very soon")
	input()

elif cookie == 4:
	print("beware a red headed man")
	input()

elif cookie == 5:
	print("thunder approaches")
	input()

else:
	print("You have no fortune, you must be dead...")
	input()
